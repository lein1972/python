def sumar_numeros(a, b):
  """Esta función suma dos números y devuelve el resultado."""
  return a + b 

# Definir los números pares
num1 =160
num2 = 4


# Llamar a la función y mostrar el resultado
resultado = sumar_numeros(num1, num2)
print(f"La suma de {num1} y {num2} es {resultado}")

def restar_numeros(a, b):
  """
  Esta función toma dos números (a y b) y devuelve el resultado de a - b.
  """
  return a - b

# --- Ejemplo de uso ---

# Definir los números
numero1 = 10
numero2 = 4

# Llamar a la función
resultado = restar_numeros(numero1, numero2)

# Imprimir el resultado
print(f"La resta de {numero1} menos {numero2} es: {resultado}") 
# Resultado impreso: La resta de 10 menos 4 es: 6

